
public class Empty{
	
	private double x, y, angle;
	
	public Empty(double xParam, double yParam, double angleParam){
		this.setX(xParam);
		this.setY(yParam);
		this.setAngle(angleParam);
	}

	public double getX(){
		return x;
	}

	public void setX(double x){
		this.x = x;
	}

	public double getY(){
		return y;
	}

	public void setY(double y){
		this.y = y;
	}

	public double getAngle(){
		return angle;
	}

	public void setAngle(double angle){
		this.angle = angle;
	}
	
	
	
	
}
